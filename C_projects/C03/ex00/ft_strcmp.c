int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	ind;

	ind = 0;
	while ((s1[ind] == s2[ind]) && ((s1[ind] != '\0') && (s2[ind] != '\0')))
	{
		ind++;
	}
	return (s1[ind] - s2[ind]);
}
